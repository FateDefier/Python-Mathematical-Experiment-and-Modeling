"""
符号变量（符号变量 = 数学里的未知数）
| 维度     | 符号变量                                              |          普通数值变量        |
| ------ | -----------------------------------------             | ----------------------- |
| **本质** | 代表一个 **抽象符号**（如 \\( x, y, a, b \\)），没有具体值 | 存储一个 **具体数值**（如 3.14、5） |
| **运算** | 做 **代数推导**（求导、积分、化简、方程求解）                | 做 **数值计算**（加减乘除）        |
| **例子** | `x + 2*x` → `3*x`（仍是符号）                          | `3 + 2*3` → `9`（直接得数）   |
| **库**  | SymPy、Mathematica、Maple                              | NumPy、纯 Python          |

作用
| 操作       | 示例                             | 结果                              |
| -------- | ------------------------------ | ------------------------------- |
| **求导**   | `diff(sin(x)*exp(x), x)`       | `exp(x)*sin(x) + exp(x)*cos(x)` |
| **积分**   | `integrate(x*exp(x), x)`       | `x*exp(x) - exp(x)`             |
| **解方程**  | `solve(x**2 - 2, x)`           | `[-sqrt(2), sqrt(2)]`           |
| **化简**   | `simplify((x**2 - 1)/(x - 1))` | `x + 1`                         |
| **代入数值** | `expr.subs(x, 3)`              | `16`                            |

赋值/求值
（1）.subs()
x, y = symbols('x y')
expr = x**2 + y
print(expr.subs({x: 3, y: 4}))   # 13
（2） .evalf() / .n() 数值化
（3）lambdify() 把符号表达式编译成可调用的 NumPy/纯 Python 函数，运行时才拿到值
f = lambdify(x, x**2, 'numpy')
print(f([1, 2, 3]))     # [1 4 9]
"""
