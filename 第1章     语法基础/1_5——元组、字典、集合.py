# =================== 复合数据类型 ====================

# 序列：有顺序的数据元素的集合体。故可通过元素所在的位置进行索引访问。常见序列：列表，元组，字符串
# 集合和字典是无顺序的数据集合体

# 元组（tuple）：不可变数据类型，元素可重复但不可改变（也可嵌套，列表，元组等）
# （1）元组是逗号分隔的对象序列（不带括号的列表），通常用小括号构建（不用小括号也可以）
# （2）元组的索引与列表相同

# t = (12345, 54321, 'hello!',['world', 'man', 23])
# print(t)
#
# print(t[1])
# print(t[1:3])
# print(t[-1][2])

# 字典（dic）：核心是以键值对形式存储数据
# （1）字典的键、值之间用冒号(:)分隔，不同键值对用逗号(,)分隔
# （2）字典用大括号{}构建，也可用dic()函数创建，或者先创建空字典，再用“[ ]”运算符以键设值
# （3）键在字典中是唯一的，不能重复，对于字符型的键要用引号引起来，键可以是单个值，亦可以是列表、元组或字典
# （4）字典不是序列，无法通过位置索引获取元素，只能通过键索引实现
# （5）字典与列表一样，都是可变类型的数据结构
# （6）修改字典必须针对“键”来设置其对应的“值”的新值，若果要增加“键值”对，只要加入新的“键值”即可(一切操作针对键完成)

