# 集合（set）：无序排列、不重复的集合体，能进行交、并、差等运算
# （1）创建：一是用大括号将多个用逗号分割的数据括起来，二是set()函数（该函数可以将字符串、列表、元组等转换为集合），!!!创建空集合必须用set()!!!
#    注：创建时不能有相同元素，若有则自动删除——可用于删除列表中大量重复的元素，先将列表用set()函数转换成集合，再用list()函数将集合装换位列表
# （2）集合的常用方法
