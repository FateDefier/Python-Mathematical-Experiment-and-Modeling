# 列表元素的增加（三个方法（method）：append、extend、insert）后面讲方法（Method）与函数(Function)的区别
# （1）append：往列表末尾添加一个元素。如a.append(element)
# （2）extend：往列表末尾添加多个元素。如a.extend([element1,element2,element3])
# （3）insert：往列表指定位置插入一个元素。如a.insert(index,element),index为要插入的位置

