# ================= 列表 =================

# 列表（list）：可变数据类型，元素可重复，元素可改变,列表创建用中括号
# ！！！一维列表 = 向量；多为列表 = 矩阵！！！

# 列表索引：用英文中括号 a[start: end: step]，列表中的一个元素可以是一个列表，故列表可以多层索引,如a[1][-1]；返回多个元素时以列表形式返回
# （1）正向索引：最左边元素索引为“0”，最右边元素索引为“元素数-1”，从左到右依次递增
# （2）负向索引：最右边元素索引为“-1”，最左边元素索引为“-元素数”，从右到左依次递减
# （3）切片：按照固定步长，连续取出多个元素，[start:end:step]，左闭右开区间
# （4）无限索引：[::step],第一个冒号表示第一个元素，第二个冒号表示最后一个元素，step步长

